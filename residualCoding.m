blockNo=1;
for i=1:N/blcoksize 
    mstart=(i-1)*blcoksize+1;  
    for j=1:N/blcoksize   
        nstart=(j-1)*blcoksize+1;  
        imBlock=zeros(blcoksize,blcoksize); % 保存每一个block灰度值

        for m=mstart:1:mstart+blcoksize-1
            for n=nstart:nstart+blcoksize-1
                mblock=mod(m,blcoksize);
                nblock=mod(n,blcoksize);
                if mblock==0 
                    mblock=blcoksize;
                end
                if nblock==0 
                    nblock=blcoksize;
                end
                imBlock(mblock,nblock)=img(m,n); % 保存每一个block  
           
            end
  
        end
            
        %%%%%%%%% 对每个Block进行zigzag扫描，得到每个block的系数向量vblock
        A_zig=zigzag(imBlock,0);
        totalCoeffs(blockNo)=nonzeroCoeff(A_zig); % 非零系数 [0 16]
        [trailingOnes(blockNo),Trailsign]=TrailingCoeff(A_zig);% 拖尾系数 [0 3]
        TrailingOnes_sign{1,blockNo}=Trailsign;
        Levels{1,blockNo}=nonzeroCoeffExceptTrailingCoeff(A_zig);
        totalZeos(blockNo)=zeroBeforeLastCoeff(A_zig); % 最后一个非零系数前零的个数 [1 15], 说明：如果16个0，说明totalCoeffs=0，在coeff_token处已编完，此处不编了。
        [run_before{1,blockNo},zerosleft{1,blockNo}]=runbeforeComputing(A_zig);
        blockNo=blockNo+1;
                
    end
end


for i=1:BlockNumber
    if i==1
        NC(i)=0;
        continue;
    end
    if i>1 && i<=N/blcoksize
        NA=totalCoeffs(i-1);
        NC(i)=NA;
        continue;
    end
    if mod(i,N/blcoksize)==1 && i~=1
        NB=totalCoeffs(i-N/blcoksize);
        NC(i)=NB;
        continue;
    end
    NA=totalCoeffs(i-1);
    NB=totalCoeffs(i-N/blcoksize);
    NC(i)=round((NA+NB)/2);
end

for i=1:BlockNumber
    if NC(i)>=0 && NC(i)<2
        NC_colum(i)=3;
    end
    if NC(i)>=2 && NC(i)<4
        NC_colum(i)=4;
    end
    if NC(i)>=4 && NC(i)<8
        NC_colum(i)=5;
    end
    if NC(i)>=8  
        NC_colum(i)=6;
    end
    if NC(i)==-1
        NC_colum(i)=7;
    end
end

