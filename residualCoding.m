%% 1.初始值设定
totalCoeffs=zeros(1,BlockNumber);% 非零系数
trailingOnes=zeros(1,BlockNumber);% 拖尾系数
totalZeos=zeros(1,BlockNumber);% 最后一个非零系数前零的个数
NC=zeros(1,BlockNumber);
NC_colum=zeros(1,BlockNumber); % NC值对应的表中的列
TrailingOnes_sign=cell(1,BlockNumber);%拖尾系数的符号
Levels=cell(1,BlockNumber);% 除了拖尾系数以外非零系数幅值Levels
Levels_code=cell(1,BlockNumber);
